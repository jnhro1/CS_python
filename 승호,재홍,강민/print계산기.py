"""
더하기 +
빼기 -
곱하기 *
나누기 /
몫만 구하기 //
나머지만 구하기 %
"""
num1 = 15
num2 = 2

print("더하기 %.d" %(num1 + num2))
print("빼기 %.d" %(num1 - num2))
print("곱하기 %.d" %(num1 *num2))
print("나누기 %.1f" %(num1 / num2))
print("몫만 구하기 %.d" %(num1 // num2))
print("나머지만 구하기 %.d" %(num1 % num2))



























