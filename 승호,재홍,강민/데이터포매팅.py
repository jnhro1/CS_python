"""
변수 : 데이터가 저장되는 공간
컴퓨터에는 메모리가 존재한다\
데이터 타입 : 문자열 숫자
정수형(소숫점 x), 실수형(소숫점 ㅇ)
변수를 만든다 = 변수를 선언한다
"""
name = "강민" #문자열(string) %s
age = 13 #정수형(int) %d
height = 151.8 #실수형(float) %f
#문자열 + 변수 출력 : 데이터 포매팅(format)
print("안녕하세요")
print("이름 : %s" %name)
print("나이 : %.1d" %age)
print("키 : %.1f" %height)

