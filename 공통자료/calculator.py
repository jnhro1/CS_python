# 출력하는 명령어 print
# 변수 : 데이터를 저장하는 공간
num1 = 15
num2 = 2

sum = num1 + num2
print("더하기 : %d" %sum)

minus = num1 - num2
print("빼기 : %d" %minus)

multi = num1 * num2
print("곱하기 : %d" %multi)

div = num1 / num2
print("나누기 : %.1f" %div)

moc = num1 // num2
print("몫 : %d" %moc)

nam = num1 % num2
print("나머지 : %d" %nam)

